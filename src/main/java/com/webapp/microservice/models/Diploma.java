package com.webapp.microservice.models;

public class Diploma {

    private Integer id;
    private String name;
    private int status;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus() {
        this.status = status;
    }
 
}